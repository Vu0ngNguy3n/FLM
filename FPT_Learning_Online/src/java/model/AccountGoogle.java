package model;

/**
 *
 * @author HP
 */
public class AccountGoogle {
    String id;
    String email;

    public AccountGoogle() {
    }

    public AccountGoogle(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
